// Generates the dive's stage line art by projecting real 3D wireframe
// geometry through a true isometric camera — the way Exea-style technical
// drawings actually get their consistency. Run `node scripts/gen-dive-art.mjs`
// and paste the emitted <g>…</g> blocks into the matching stage SVGs in
// index.html. Proportions reference a real dual-fan card: 267×112 mm PCB.

// ---------------------------------------------------------------- camera
const COS30 = Math.cos(Math.PI / 6);
const SIN30 = 0.5;
// true isometric: x right-down, z left-down, y up
function proj([x, y, z], scale, ox, oy) {
  return [
    ox + (x - z) * COS30 * scale,
    oy + (x + z) * SIN30 * scale - y * scale,
  ];
}

const fmt = n => (Math.round(n * 10) / 10).toString();

class Draw {
  constructor(scale, ox, oy) { this.s = scale; this.ox = ox; this.oy = oy; this.out = { w: [], d: [], amber: [] }; }
  line(a, b, cls = "w") {
    const p = proj(a, this.s, this.ox, this.oy), q = proj(b, this.s, this.ox, this.oy);
    this.out[cls].push(`M${fmt(p[0])} ${fmt(p[1])}L${fmt(q[0])} ${fmt(q[1])}`);
  }
  poly(pts, cls = "w", close = true) {
    const pr = pts.map(v => proj(v, this.s, this.ox, this.oy));
    let d = `M${fmt(pr[0][0])} ${fmt(pr[0][1])}`;
    for (let i = 1; i < pr.length; i++) d += `L${fmt(pr[i][0])} ${fmt(pr[i][1])}`;
    if (close) d += "Z";
    this.out[cls].push(d);
  }
  /** horizontal circle at height y, centered (cx, cz) */
  circle(cx, y, cz, r, cls = "w", segs = 40) {
    const pts = [];
    for (let i = 0; i < segs; i++) {
      const a = (i / segs) * Math.PI * 2;
      pts.push([cx + r * Math.cos(a), y, cz + r * Math.sin(a)]);
    }
    this.poly(pts, cls);
  }
  /** box: min corner (x,y,z), size (w,h,d); wireframe with verticals */
  box(x, y, z, w, h, d, cls = "w", verticals = true) {
    const b = [[x, y, z], [x + w, y, z], [x + w, y, z + d], [x, y, z + d]];
    const t = b.map(([a, , c]) => [a, y + h, c]);
    this.poly(t, cls);
    this.poly(b, cls);
    if (verticals) for (let i = 0; i < 4; i++) this.line(b[i], t[i], cls);
  }
  emit(label) {
    const g = Object.entries(this.out)
      .filter(([, v]) => v.length)
      .map(([cls, v]) => `  <path class="${cls}" d="${v.join(" ")}"/>`)
      .join("\n");
    console.log(`\n<!-- ${label} (generated by scripts/gen-dive-art.mjs) -->\n${g}`);
  }
}

// ---------------------------------------------------------- S1 · GPU card
// model units = mm; PCB 267 × 112, cooler with two 92 mm axial fans.
{
  const D = new Draw(2.55, 429, 210);
  const W = 267, DEP = 112, PCB = 3;

  // pcb slab
  D.box(0, 0, 0, W, PCB, DEP, "w");
  // backplate lip
  D.box(4, PCB, 4, W - 8, 1.5, DEP - 8, "d", false);

  // shroud: covers the left 2/3, die area exposed on the right
  const SH = { x: 0, w: 176, h: 38 };
  D.box(SH.x, PCB, 0, SH.w, SH.h, DEP, "w");

  // fans: two 92 mm axial, blades as arcs
  for (const fx of [46, 134]) {
    const cy = PCB + SH.h + 0.1, cz = DEP / 2;
    D.circle(fx, cy, cz, 42, "w");
    D.circle(fx, cy, cz, 39, "d");
    D.circle(fx, cy, cz, 11, "w");
    for (let b = 0; b < 9; b++) {
      const a0 = (b / 9) * Math.PI * 2;
      const pts = [];
      for (let t = 0; t <= 6; t++) {
        const a = a0 + (t / 6) * 0.62;
        const r = 12 + (t / 6) * 26;
        pts.push([fx + r * Math.cos(a), cy, cz + r * Math.sin(a)]);
      }
      D.poly(pts, "d", false);
    }
  }

  // fin stack peeking under the shroud along the front edge
  for (let x = 8; x < SH.w - 6; x += 7) D.line([x, PCB + 2, DEP + 0.1], [x, PCB + SH.h - 6, DEP + 0.1], "d");

  // exposed pcb: the die + memory footprints (dive target zone)
  D.box(196, PCB, 34, 34, 2.4, 34, "amber");           // GPU die
  for (const [mx, mz] of [[188, 12], [238, 12], [188, 76], [238, 76], [160, 34], [160, 60]])
    D.box(mx, PCB, mz, 16, 1.6, 12, "d", false);        // GDDR footprints
  // vrm row near tail
  for (let z = 14; z <= 90; z += 11) D.box(250, PCB, z, 9, 4, 8, "d", false);

  // pcie edge fingers
  D.line([12, -0.1, 0], [12 + 89, -0.1, 0], "w");
  for (let x = 14; x < 99; x += 3.4) D.line([x, 0, 0], [x, -4.5, 0], "d");
  // bracket at the io end
  D.poly([[0, -8, 0], [0, 48, 0], [0, 48, DEP * 0.36], [0, -8, DEP * 0.36]], "w");
  D.line([0, 8, 8], [0, 40, 8], "d");
  D.line([0, 8, 20], [0, 40, 20], "d");
  // 8-pin sockets on the spine
  D.box(210, PCB + 2.4, -0.5, 22, 9, 7, "d");
  D.box(236, PCB + 2.4, -0.5, 22, 9, 7, "d");

  D.emit("S1 · GPU card wireframe");
}

// ------------------------------------------------- S2 · package (top-down)
// flat orthographic: emit content-space paths directly (no projection).
{
  const P = { w: [], d: [], amber: [] };
  const R = (x, y, w, h, cls) => P[cls].push(`M${x} ${y}h${w}v${h}h${-w}Z`);
  const L = (x1, y1, x2, y2, cls) => P[cls].push(`M${x1} ${y1}L${x2} ${y2}`);

  // substrate with chamfered corner
  P.w.push("M300 120 h560 l40 40 v480 h-600 Z");
  // interposer
  R(360, 200, 480, 400, "d");
  // HBM stacks: 4 per side, real MI-style flanking
  for (let i = 0; i < 4; i++) {
    R(374, 214 + i * 96, 88, 80, "w");
    R(738, 214 + i * 96, 88, 80, "w");
    for (let r = 1; r < 4; r++) {
      L(374, 214 + i * 96 + r * 20, 462, 214 + i * 96 + r * 20, "d");
      L(738, 214 + i * 96 + r * 20, 826, 214 + i * 96 + r * 20, "d");
    }
  }
  // compute die between the stacks: chiplet grid 2×4
  R(486, 214, 228, 372, "w");
  for (let i = 0; i < 4; i++) for (let j = 0; j < 2; j++) R(498 + j * 108, 226 + i * 92, 96, 80, "d");
  // cell-grid texture inside the two center chiplets (the dive target)
  for (let x = 504; x <= 588; x += 12) L(x, 322, x, 398, "d");
  for (let y = 322; y <= 398; y += 12) L(498, y, 594, y, "d");
  // caps along the bottom edge
  for (let x = 320; x < 840; x += 26) R(x, 622, 16, 8, "d");
  // ball-grid dots along top edge
  for (let x = 316; x < 852; x += 22) P.d.push(`M${x} 140 h6`);

  const g = Object.entries(P).filter(([, v]) => v.length)
    .map(([cls, v]) => `  <path class="${cls}" d="${v.join(" ")}"/>`).join("\n");
  console.log(`\n<!-- S2 · package floorplan (generated) -->\n${g}`);
}

// ------------------------------------------------- S5 · dendrite poster
// flat projection of src/dendrite.mjs — the reduced-motion / no-WebGL still
// for the dive finale. Runtime growth uses the same generator in 3D.
import { genDendrite } from "../src/dendrite.mjs";
{
  const segs = genDendrite();
  const P = { w: [], d: [], amber: [] };
  const X = v => fmt(600 + v * 62);
  const Y = v => fmt(400 - v * 62);
  for (const s of segs) P[s.cls].push(`M${X(s.a[0])} ${Y(s.a[1])}L${X(s.b[0])} ${Y(s.b[1])}`);
  const g = Object.entries(P).filter(([, v]) => v.length)
    .map(([cls, v]) => `  <path class="${cls}" d="${v.join(" ")}"/>`).join("\n");
  console.log(`\n<!-- S5 · geometric dendrite (generated) -->\n${g}`);
}
